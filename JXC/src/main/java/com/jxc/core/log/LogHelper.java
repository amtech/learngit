package com.jxc.core.log;

import org.apache.log4j.Logger;

/**
 * @FileName: LogHelper.java
 * @Author: WuF
 * @Version: 1.0
 * @Date: 2012-3-21下午5:20:48
 * @Description: 日志类,按类型获取日志
 * @Others: 
 * @FuntionList:
 * @History:
 * @Category:
 */
public class LogHelper {

	public static Logger getCommonLogger() {
		return Logger.getLogger("commonLogger");
	}

	public static Logger getDevelopmentLogger() {
		return Logger.getLogger("developmentLogger");
	}

	public static Logger getLogger(String logName) {
		return Logger.getLogger(logName);
	}
}
